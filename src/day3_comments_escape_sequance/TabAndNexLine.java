package day3_comments_escape_sequance;

public class TabAndNexLine {

    public static void main (String [] args){


        //This is without any tab
        System.out.println("");


        //This is without one tab
        System.out.println("\tThis is tabbed once");

        //This is with two tab
        System.out.println("\t\tThis is tabbed twice");


        //Here is tabbed without escape sequence
        System.out.println("      This is tabbed without sequence");

        // examples of new line
        System.out.println("");





    }
}
